import { useCallback, useRef, useState, useEffect } from 'react';
import { app } from '../utils/cloudbase';

/**
 * useRealtimeSessionWebSocket Hook - Âü∫‰∫éWebSocketÁöÑÂÆûÊó∂ÂØπËØù
 * ÈÄÇÈÖç‰∏≠ËΩ¨APIÁöÑWebSocketËøûÊé•ÊñπÂºèÔºå‰∏çÈúÄË¶Åephemeral key
 */
export function useRealtimeSessionWebSocket(options = {}) {
  const {
    onConnectionChange,
    onError,
    onMessage,
    onAudioReceived,
    initialAgent = null
  } = options;

  const wsRef = useRef(null);
  const audioContextRef = useRef(null);
  const mediaStreamRef = useRef(null);
  const [status, setStatus] = useState('DISCONNECTED');
  const [isConnected, setIsConnected] = useState(false);
  const [isConnecting, setIsConnecting] = useState(false);
  const [error, setError] = useState(null);
  const [conversationHistory, setConversationHistory] = useState([]);
  const [currentAgent, setCurrentAgent] = useState(initialAgent);
  const [volumeLevel, setVolumeLevel] = useState(0);
  const [isSilent, setIsSilent] = useState(true);

  /**
   * Êõ¥Êñ∞ËøûÊé•Áä∂ÊÄÅ
   */
  const updateStatus = useCallback((newStatus) => {
    console.log(`üîÑ Status change: ${status} -> ${newStatus}`);
    setStatus(newStatus);
    setIsConnected(newStatus === 'CONNECTED');
    setIsConnecting(newStatus === 'CONNECTING');
    onConnectionChange?.(newStatus);
  }, [status, onConnectionChange]);

  /**
   * Â§ÑÁêÜÈîôËØØ
   */
  const handleError = useCallback((error) => {
    console.error('‚ùå WebSocket session error:', error);
    const errorMessage = error?.message || error?.toString() || 'Unknown error';
    setError(errorMessage);
    setIsConnecting(false);
    onError?.(errorMessage);
  }, [onError]);

  /**
   * ÂàùÂßãÂåñÈü≥È¢ë‰∏ä‰∏ãÊñáÂíåÈ∫¶ÂÖãÈ£é
   */
  const initializeAudio = useCallback(async () => {
    try {
      console.log('üé§ Initializing audio...');
      
      // ÂàõÂª∫Èü≥È¢ë‰∏ä‰∏ãÊñá
      audioContextRef.current = new (window.AudioContext || window.webkitAudioContext)();
      
      // Ëé∑ÂèñÈ∫¶ÂÖãÈ£éÊùÉÈôê
      const stream = await navigator.mediaDevices.getUserMedia({ 
        audio: {
          sampleRate: 24000,
          channelCount: 1,
          echoCancellation: true,
          noiseSuppression: true
        } 
      });
      
      mediaStreamRef.current = stream;
      console.log('‚úÖ Audio initialized successfully');
      return stream;
    } catch (error) {
      console.error('‚ùå Failed to initialize audio:', error);
      throw new Error(`È∫¶ÂÖãÈ£éÂàùÂßãÂåñÂ§±Ë¥•: ${error.message}`);
    }
  }, []);

  /**
   * ËøûÊé•Âà∞WebSocket
   */
  const connect = useCallback(async () => {
    if (wsRef.current) {
      console.log('‚ö†Ô∏è Already connected or connecting');
      return;
    }

    try {
      updateStatus('CONNECTING');
      setError(null);

      // ÂàùÂßãÂåñÈü≥È¢ë
      await initializeAudio();

      // Ëé∑ÂèñÁéØÂ¢É‰ø°ÊÅØ
      const envResponse = await app.callFunction({
        name: 'realtime-proxy',
        data: { action: 'test_connection' }
      });

      if (!envResponse.result?.success) {
        throw new Error('Êó†Ê≥ïËøûÊé•Âà∞‰∏≠ËΩ¨API');
      }

      // ÊûÑÂª∫WebSocket URL - Ê†πÊçÆÊñáÊ°£Â∞ùËØïÊ≠£Á°ÆÁöÑÁ´ØÁÇπ
      // ‰ªéÊñáÊ°£ÁúãÔºåWebSocketÁ´ØÁÇπÂ∫îËØ•ÊòØ wss://api.newapi.pro/v1/realtime
      // ‰ΩÜÊàë‰ª¨‰ΩøÁî®ÁöÑÊòØ chataiapi.comÔºåÈúÄË¶ÅÁ°ÆËÆ§Ê≠£Á°ÆÁöÑÊ†ºÂºè
      
      const baseUrl = envResponse.result.connection.baseURL || 'https://www.chataiapi.com/v1';
      console.log('üîç Base URL from API:', baseUrl);
      
      // Â∞ùËØïÂ§öÁßçÂèØËÉΩÁöÑWebSocket URLÊ†ºÂºè
      const possibleUrls = [
        // Ê†áÂáÜÊ†ºÂºè - ‰ΩøÁî®Áî®Êà∑Êèê‰æõÁöÑÊ≠£Á°ÆÊ®°Âûã
        baseUrl.replace('https://', 'wss://').replace('http://', 'ws://') + '/realtime?model=gpt-4o-realtime-preview',
        // ‰∏çÂ∏¶Êü•ËØ¢ÂèÇÊï∞
        baseUrl.replace('https://', 'wss://').replace('http://', 'ws://') + '/realtime',
        // Áõ¥Êé•‰ΩøÁî®ÂüüÂêç
        'wss://www.chataiapi.com/v1/realtime',
        'wss://www.chataiapi.com/v1/realtime?model=gpt-4o-realtime-preview'
      ];
      
      console.log('üîç Possible WebSocket URLs:', possibleUrls);
      
      // ÂÖà‰ΩøÁî®Á¨¨‰∏Ä‰∏™URL
      const wsUrl = possibleUrls[0];
      
      console.log('üîó Connecting to WebSocket:', wsUrl);

      // ÂàõÂª∫WebSocketËøûÊé• - ÂÖàÂ∞ùËØïÁÆÄÂçïÁöÑËøûÊé•ÊñπÂºè
      const apiKey = 'sk-MVwM0Y77CDZbTMT0cFoDe5WSZuYZk1G64dMWE6hpBitqgkgT';
      
      // Â∞ùËØï‰∏çÂêåÁöÑËøûÊé•ÊñπÂºè
      console.log('üîó Attempting WebSocket connection...');
      console.log('üîó URL:', wsUrl);
      
      // Ê†πÊçÆÊñáÊ°£‰ΩøÁî®Ê≠£Á°ÆÁöÑÂçèËÆÆÊ†ºÂºè
      wsRef.current = new WebSocket(wsUrl, [
        "realtime",
        // ËÆ§ËØÅ - ‰ΩøÁî®ÊñáÊ°£‰∏≠ÁöÑÊ≠£Á°ÆÊ†ºÂºè
        "openai-insecure-api-key." + apiKey,
        // BetaÂçèËÆÆÔºåÂøÖÈúÄ
        "openai-beta.realtime-v1"
      ]);
      
      console.log('‚úÖ WebSocket created with protocols:', [
        "realtime",
        "openai-insecure-api-key." + apiKey,
        "openai-beta.realtime-v1"
      ]);

      // ËÆæÁΩÆ‰∫ã‰ª∂ÁõëÂê¨Âô®
      wsRef.current.onopen = () => {
        console.log('‚úÖ WebSocket connected');
        updateStatus('CONNECTED');
        
        // ÂèëÈÄÅ‰ºöËØùÈÖçÁΩÆ
        const sessionConfig = {
          type: 'session.update',
          session: {
            modalities: ['text', 'audio'],
            instructions: `‰Ω†ÊòØ‰∏Ä‰∏™‰∏ì‰∏öÁöÑËã±ËØ≠Â≠¶‰π†Âä©Êâã„ÄÇ‰Ω†ÁöÑ‰ªªÂä°ÊòØÂ∏ÆÂä©Áî®Êà∑ÊèêÈ´òËã±ËØ≠Ê∞¥Âπ≥„ÄÇ

ÁâπÁÇπÔºö
- ÂèãÂ•Ω„ÄÅËÄêÂøÉ„ÄÅÈºìÂä±Â≠¶‰π†ËÄÖ
- Ê†πÊçÆÁî®Êà∑Ê∞¥Âπ≥Ë∞ÉÊï¥ËØ≠Ë®ÄÈöæÂ∫¶
- Êèê‰æõÂÆûÁî®ÁöÑÂ≠¶‰π†Âª∫ËÆÆ
- Á∫†Ê≠£ÂèëÈü≥ÂíåËØ≠Ê≥ïÈîôËØØ
- ËøõË°åÊúâÊÑè‰πâÁöÑÂØπËØùÁªÉ‰π†

ËØ∑Áî®Ëá™ÁÑ∂„ÄÅ‰∫≤ÂàáÁöÑÊñπÂºè‰∏éÁî®Êà∑‰∫§ÊµÅÔºå‰∏ìÊ≥®‰∫éÂ∏ÆÂä©‰ªñ‰ª¨ÊèêÈ´òËã±ËØ≠ÊäÄËÉΩ„ÄÇ`,
            voice: 'nova',
            input_audio_format: 'pcm16',
            output_audio_format: 'pcm16',
            input_audio_transcription: {
              model: 'whisper-1'
            },
            turn_detection: {
              type: 'server_vad',
              threshold: 0.5,
              prefix_padding_ms: 300,
              silence_duration_ms: 500
            },
            temperature: 0.7,
            max_response_output_tokens: 'inf'
          }
        };
        
        wsRef.current.send(JSON.stringify(sessionConfig));
        console.log('üì§ Sent session configuration');
      };

      wsRef.current.onmessage = (event) => {
        try {
          const message = JSON.parse(event.data);
          console.log('üì• Received message:', message.type, message);
          
          // Â§ÑÁêÜ‰∏çÂêåÁ±ªÂûãÁöÑÊ∂àÊÅØ
          switch (message.type) {
            case 'session.updated':
              console.log('‚úÖ Session updated:', message.session);
              break;
              
            case 'conversation.item.input_audio_transcription.completed':
              console.log('üé§ Input transcription:', message.transcript);
              setConversationHistory(prev => [...prev, {
                id: Date.now(),
                role: 'user',
                text: message.transcript,
                timestamp: new Date()
              }]);
              break;
              
            case 'response.text.delta':
              console.log('üìù Text delta:', message.delta);
              onMessage?.(message);
              break;
              
            case 'response.audio.delta':
              console.log('üîä Audio delta received');
              onAudioReceived?.(message.delta);
              break;
              
            case 'response.text.done':
              console.log('‚úÖ Response completed:', message.text);
              setConversationHistory(prev => [...prev, {
                id: Date.now(),
                role: 'assistant',
                text: message.text,
                timestamp: new Date()
              }]);
              break;
              
            case 'error':
              console.error('‚ùå Server error:', message.error);
              handleError(new Error(message.error.message));
              break;
              
            default:
              console.log('üìã Other message type:', message.type);
          }
        } catch (error) {
          console.error('‚ùå Failed to parse message:', error);
        }
      };

      wsRef.current.onerror = (error) => {
        console.error('‚ùå WebSocket error:', error);
        console.error('‚ùå WebSocket URL was:', wsUrl);
        console.error('‚ùå WebSocket protocols were:', [
          'realtime',
          `openai-insecure-api-key.${apiKey}`,
          'openai-beta.realtime-v1'
        ]);
        handleError(new Error('WebSocketËøûÊé•ÈîôËØØ'));
      };

      wsRef.current.onclose = (event) => {
        console.log('üîå WebSocket closed:', event.code, event.reason);
        
        // Ëß£ÈáäÂ∏∏ËßÅÁöÑÂÖ≥Èó≠‰ª£Á†Å
        const closeReasons = {
          1000: 'Normal closure',
          1001: 'Going away',
          1002: 'Protocol error',
          1003: 'Unsupported data',
          1006: 'No status code',
          1007: 'Invalid data',
          1008: 'Policy violation',
          1009: 'Message too large',
          1010: 'Extension expected',
          1011: 'Server error',
          1015: 'TLS failure'
        };
        
        console.log('üîå Close reason:', closeReasons[event.code] || 'Unknown');
        
        if (event.code !== 1000) {
          handleError(new Error(`WebSocketËøûÊé•ÂºÇÂ∏∏ÂÖ≥Èó≠: ${closeReasons[event.code] || event.code} - ${event.reason}`));
        }
        
        wsRef.current = null;
        updateStatus('DISCONNECTED');
        
        // Ê∏ÖÁêÜÈü≥È¢ëËµÑÊ∫ê
        if (mediaStreamRef.current) {
          mediaStreamRef.current.getTracks().forEach(track => track.stop());
          mediaStreamRef.current = null;
        }
        if (audioContextRef.current) {
          audioContextRef.current.close();
          audioContextRef.current = null;
        }
      };

    } catch (error) {
      console.error('‚ùå Failed to connect:', error);
      handleError(error);
      updateStatus('DISCONNECTED');
    }
  }, [updateStatus, handleError, initializeAudio, onMessage, onAudioReceived]);

  /**
   * Êñ≠ÂºÄËøûÊé•
   */
  const disconnect = useCallback(async () => {
    try {
      console.log('üîå Disconnecting from WebSocket...');
      
      if (wsRef.current) {
        wsRef.current.close();
        wsRef.current = null;
      }
      
      // Ê∏ÖÁêÜÈü≥È¢ëËµÑÊ∫ê
      if (mediaStreamRef.current) {
        mediaStreamRef.current.getTracks().forEach(track => track.stop());
        mediaStreamRef.current = null;
      }
      if (audioContextRef.current) {
        audioContextRef.current.close();
        audioContextRef.current = null;
      }
      
      setCurrentAgent(null);
      setError(null);
      updateStatus('DISCONNECTED');
      
      console.log('‚úÖ Disconnected successfully');
    } catch (error) {
      console.error('‚ùå Error during disconnect:', error);
    }
  }, [updateStatus]);

  /**
   * ÂèëÈÄÅÊñáÊú¨Ê∂àÊÅØ
   */
  const sendUserText = useCallback((text) => {
    if (!wsRef.current || wsRef.current.readyState !== WebSocket.OPEN) {
      throw new Error('WebSocket not connected');
    }
    
    console.log('üì§ Sending user text:', text);
    
    const message = {
      type: 'conversation.item.create',
      item: {
        type: 'message',
        role: 'user',
        content: [
          {
            type: 'input_text',
            text: text
          }
        ]
      }
    };
    
    wsRef.current.send(JSON.stringify(message));
    
    // Ëß¶ÂèëÂìçÂ∫îÁîüÊàê
    const responseCreate = {
      type: 'response.create'
    };
    
    wsRef.current.send(JSON.stringify(responseCreate));
  }, []);

  /**
   * ÂèëÈÄÅÈü≥È¢ëÊï∞ÊçÆÔºàÂç†‰ΩçÂÆûÁé∞Ôºâ
   */
  const sendAudioData = useCallback((audioData) => {
    if (!wsRef.current || wsRef.current.readyState !== WebSocket.OPEN) {
      return;
    }
    
    const message = {
      type: 'input_audio_buffer.append',
      audio: audioData // Base64ÁºñÁ†ÅÁöÑÈü≥È¢ëÊï∞ÊçÆ
    };
    
    wsRef.current.send(JSON.stringify(message));
  }, []);

  /**
   * ‰∏≠Êñ≠ÂΩìÂâçÂìçÂ∫î
   */
  const interrupt = useCallback(() => {
    if (wsRef.current && wsRef.current.readyState === WebSocket.OPEN) {
      const message = {
        type: 'response.cancel'
      };
      wsRef.current.send(JSON.stringify(message));
      console.log('‚èπÔ∏è Interrupted current response');
    }
  }, []);

  /**
   * Ê∏ÖÈô§ÂéÜÂè≤ËÆ∞ÂΩï
   */
  const clearHistory = useCallback(() => {
    setConversationHistory([]);
    setError(null);
    console.log('üóëÔ∏è Conversation history cleared');
  }, []);

  /**
   * ÈùôÈü≥/ÂèñÊ∂àÈùôÈü≥ÔºàÂç†‰ΩçÂÆûÁé∞Ôºâ
   */
  const mute = useCallback((shouldMute) => {
    if (mediaStreamRef.current) {
      mediaStreamRef.current.getAudioTracks().forEach(track => {
        track.enabled = !shouldMute;
      });
      console.log(shouldMute ? 'üîá Muted' : 'üîä Unmuted');
    }
  }, []);

  // Ê∏ÖÁêÜËµÑÊ∫ê
  useEffect(() => {
    return () => {
      if (wsRef.current) {
        wsRef.current.close();
      }
      if (mediaStreamRef.current) {
        mediaStreamRef.current.getTracks().forEach(track => track.stop());
      }
      if (audioContextRef.current) {
        audioContextRef.current.close();
      }
    };
  }, []);

  return {
    // Áä∂ÊÄÅ
    status,
    isConnected,
    isConnecting,
    error,
    currentAgent,
    conversationHistory,
    volumeLevel,
    isSilent,

    // ÊñπÊ≥ï
    connect,
    disconnect,
    sendUserText,
    sendAudioData,
    mute,
    interrupt,
    clearHistory,

    // ÂÜÖÈÉ®ÂºïÁî®ÔºàÁî®‰∫éË∞ÉËØïÔºâ
    wsRef: wsRef.current
  };
}